#include <stdio.h>
#include <stdlib.h>

#define N 10
//--------------------------------------------------------------------------------------------------------
void print_arr(int A[])
{
    for(int i = 0; i < N; i++ )
        printf(" %3d ",A[i]& 1);
    printf("\n");

}
//--------------------------------------------------------------------------------------------------------
int main()
{
    int A[N] = { 0, 11, 20, 31, 40,
                51, 60, 71, 80, 91 };
    int B[N] = {0};
//--------------------------------------------------------------------------------------------------------
//copy
    printf("<---------------Copy arr--------------->\n");
    for(int i = 0; i < N; i++)
        B[i] = A[i];

    print_arr(A);
    print_arr(B);
//--------------------------------------------------------------------------------------------------------
//revers
printf("<---------------Revers arr--------------->\n");
for(int i = 0; i < N; i++)
        B[i] = A[N - 1 - i];

    print_arr(A);
    print_arr(B);
//--------------------------------------------------------------------------------------------------------
printf("<---------------Revers one arr--------------->\n");
int tmp=0;
for(int i = 0; i < N / 2; i++)
        {
            tmp      = A[i];
            A[i]     = A[N-1-i];
            A[N-1-i] = tmp;
        }


    print_arr(A);
//--------------------------------------------------------------------------------------------------------
printf("<---------------shift is cyclic  left one arr--------------->\n");
    tmp=A[0];
    for(int i = 0; i < N-1; i++)
        A[i]=A[i+1];
    A[N-1] = tmp;
    print_arr(A);
//--------------------------------------------------------------------------------------------------------
printf("<---------------shift is cyclic  right one arr--------------->\n");
    tmp=A[N];
    for(int i = N-1; i >= 0; i--)
        A[i]=A[i-1];
    A[0] = tmp;
    print_arr(A);
//--------------------------------------------------------------------------------------------------------
    return 0;
}
